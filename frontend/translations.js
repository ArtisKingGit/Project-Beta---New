export function applyTranslations(lang = 'en') {
    const t = translations[lang] || translations.en;

    // Map of IDs to translation keys (optional direct mapping)
    const elementMap = {
        'sidebar-dashboard': t.dashboard,
        'sidebar-recommendations': t.recommendations,
        'sidebar-manage': t.manage_farms,
        'sidebar-scanner': t.crop_scanner,
        'sidebar-history': t.history,
        'sidebar-settings': t.settings,
        'sidebar-logout-text': t.logout,
        'header-title': t.account_settings,
        'saveBtn-text': t.save_profile,
        'widget-inventory': t.resource_inventory,
        'widget-tasks': t.task_manager,
        'widget-weather': t.weather_info,
        'widget-scans': t.recent_scans,
        'sidebar-redo': t.redo_tutorial
    };

    // Apply direct mappings if elements exist
    Object.entries(elementMap).forEach(([id, text]) => {
        const el = document.getElementById(id);
        if (el && text) el.textContent = text;
    });

    // Update elements with [data-i18n] attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            const tags = ['SPAN', 'H1', 'H2', 'H3', 'H4', 'P', 'BUTTON', 'LABEL', 'STRONG', 'DIV'];
            if (tags.includes(el.tagName)) {
                el.textContent = t[key];
            } else if (el.tagName === 'INPUT' && el.placeholder) {
                el.placeholder = t[key];
            }
        }
    });

    // Update specific sidebar links if they don't have data attributes yet
    // I will add [data-i18n] to the HTML files in the next step
}

export function convertUnit(value, from, to) {
    if (from === to) return value;
    if (from === 'acres' && to === 'hectares') return (value * 0.404686).toFixed(2);
    if (from === 'hectares' && to === 'acres') return (value / 0.404686).toFixed(2);
    return value;
}
